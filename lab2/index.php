                <h2>The Damaging Effects of Deforestation</h2>
                <!-- P1: about slash and burn method -->
                <p>As we know by now, deforestation is occuring every day on our earth as a result of construction, agriculture, and other human activity.
                    One type of deforestation, used for agriculture, is called the slash and burn method, in which farmers burn plots of forests to clear farmland and fertilize the ground with the ash from the burnt trees. This soil, however, becomes 
                    unusable after a few years, so farmers must move to another location and repeat the method.</p>
                <!--P2: about the effects of slash and burn method -->
                <p>Trees hold carbon dioxide in their wood. When they are burned, this carbon dioxide goes up into the atmosphere, resulting in increased levels
                    of global warming. With the slash and burn method, large plots of land are burned, used for agriculture, then abandoned. The temporary positive effects
                    of the slash and burn method worsens deforestation because farmers are forced to leave the land and find another plot to clear out every few years. This justs
                    adds to the accumulation of carbon dioxide in our atmosphere, accelerating global warming to concerning levels.</p>
                
                <!--P3: other negative effects of deforestation -->
                <p>In addition to the unnerving amounts of carbon dioxide in the atmosphere, deforestation also contributes soil erosion and flooding. Trees provide
                    nutrients to soil which helps support the growth of other plants in the forest. When farmers can no longer use their land, they leave behind barren
                    and infertile soil, which is more susceptable to flooding and cannot support forest life. Deforestation also destroys animal habitats, as many animals
                    take shelter under trees and canopies. Deforestation also causes drastic temperature changes, which makes animal's habitats more inhospitable.</p>
