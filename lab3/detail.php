            </section>
            <section>
                <h3>1850-1920</h3>
                <p>Rapid population growth and the invention of the rubber tire in 1914 led to a new wave of deforestation, causing tree loss to rise 50%.
                    Trees lost per decade increased to around 74 million acres.</p>
            </section>
            <section>
                <h3>1920-1930</h3>
                <p>The Great Depression caused many farmers to leave their land behind, allowing forests some time to regrow.</p>
            </section>
            <section>
                <h3>1940-1980</h3>
                <p>The construction of the Belem-Brasilia Highway (1950) and the Transamazon Highway (1970) in Brazil caused deforestation rates to increase futher.
                    After these projects, tree loss rose to more than 296 million acres per decade.</p>
            </section>
            <section>
                <h3>1980</h3>
                <p>Global deforestation reaches its peak: up to 370 million acres of trees lost per decade. Government investments
                    in infrastructure and agriculture encouraged forest destruction.</p>
            </section>
            <section>
                <h3>1990-2000</h3>
                <p>Eventually, countries reach a <strong>forest transition point</strong> where more trees are regrown than lost. Around
                the end of 1990, forests reached their transition points, causing tree loss to decrease to 128 million in 2000.</p>
            </section>
